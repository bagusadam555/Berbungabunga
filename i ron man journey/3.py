# yuk belajar if else biar bisa milih doi yang baik
pertanyaan='Dia mau nikah dengan saya?'
jawaban=input(pertanyaan)
print(jawaban)
if jawaban=='ya':
    print('alhamdulillah')
elif jawaban=='ok':
    print('gas nikah langsung besok kalo ga ujan yaa')
elif jawaban=='mau banget':
    print(':)')
else :
    print('alhamdulillah Tuhan melindungi saya dan akan mengganti dengan yang jauh lebih baik')